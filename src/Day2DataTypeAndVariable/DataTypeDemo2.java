package Day2DataTypeAndVariable;

public class DataTypeDemo2 {

	public static void main(String[] args) {
		
		
		//Numeric data type
		
		int a=68768547;
		System.out.println("The value of a is:"+a);
		
		
		long x= 1998834566;
		System.out.println("Value of x is:" +x);
		
		byte b=-125;
		System.out.println("The value of b is "+b);
		
		short sh=32766;
		System.out.println("The value of sh is:"+b);
		
		// Decimal data type: float,double
		
		float M= 6.980F;
		System.out.println("The value of M is:" +M);
		
		double d=123.9876540987612345;
		System.out.println("The value if d is:"+d);
		
		double dou=786;
		System.out.println("The value of dou is:"+dou);
		
		//int e=12.98; // not valid
		
		char Grade='A';
		System.out.println("The grade of student is :"+Grade);
		
		boolean bo=true;
		System.out.println(bo);
		
		String name="Anjali Jain";
		System.out.println("My name is: "+name);
		
		String str="A";
		System.out.println("My grade is:"+str);
		
		//Char ch='abc'; //invalid
		//String sr=true;//invalid
		
		String sr="true";//valid
		System.out.println(sr);
		
		
				
		
		
				
		
		
		
		
		
		

	}

}
