package Practice;

public class EvenOdd {

	public static void main(String[] args) {
		int num=10;
		if(num%2==0)
		{
			System.out.println("Number is even "+num);
		}
		else if(num==0)
		{
		    System.out.println("Number is zero"+num);	
		}
		else
		{
			System.out.println("Number is odd"+num);
		}

	}

}
